
# Demande de nom de l'utilisateur
nom = input("Quel est ton nom ? ")

# Affichage de la réponse
print("Bonjour", nom, ", comment vas-tu ?")

# Demande de l'état d'esprit de l'utilisateur
mood = input("Je vais bien, et toi ? ")

# Affichage de la réponse
if mood == "bien":
    print("Je suis content de l'entendre !")
else:
    print("Je suis désolé de l'apprendre. Est-ce que tu veux en parler ?")



